#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	char *tmp;

	tmp = s1;
	if (n)
	{
		while (*s1 != '\0')
			s1++;
		while (*s2 != '\0')
		{
			*s1++ = *s2++;
			if (--n == 0)
			{
				*s1 = 0;
				return (tmp);
			}
		}
		*s1 = *s2;
	}
	return (tmp);
}
